package simplesurvey;

public class SurveyDto {
	private int num;
	private int age;
	private String gender;
	private int salary;
	private int salarydesired;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public int getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	public int getSalarydesired() {
		return salarydesired;
	}
	public void setSalarydesired(int salarydesired) {
		this.salarydesired = salarydesired;
	}
}
